package com.example.demo.Model;

public class DataList {


    String name;
    String fav_cricketer;
    String colors;
    int id;
    String date;


    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getFav_cricketer() {
        return fav_cricketer;
    }

    public void setFav_cricketer(String fav_cricketer) {
        this.fav_cricketer = fav_cricketer;
    }

    public String getColors() {
        return colors;
    }

    public void setColors(String colors) {
        this.colors = colors;
    }
}
